<?php

namespace Jan\MOCBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * PlayerConnectionType
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class PlayerConnectionType
{
    const LOCALHOST = 0;
    const SSH = 1;


    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;





    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return PlayerConnectionType
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
